package pack2;

public class typecas1 {
	
	
	static float a=10;
	static int b=(int)a;     // explicitly type casting
	static double c=b;           // implicitly type casting
	static short d=(short)c;   // explicitly type casting

	public static void main(String[] args)
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

	}

}
